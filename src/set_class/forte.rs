/// Look up the Forte number for a given prime form.
pub fn lookup(prime_form: &[u8]) -> Option<String> {
    let card = prime_form.len();
    let table: &[(&[u8], &str)] = match card {
        3 => &TRICHORDS,
        4 => &TETRACHORDS,
        _ => return None,
    };
    for &(pf, name) in table {
        if pf == prime_form {
            return Some(name.to_string());
        }
    }
    None
}

